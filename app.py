def greet(name):
    return f"Hello, {name}!"

if __name__ == "__main__":
    name = input("Enter your name: ")
    print(greet(name))
# This is a simple greeting function
# that takes a name as input and returns a greeting message.
# The script also includes a main block that prompts the user for their name
# and prints the greeting message.
# The greet function is reusable and can be imported into other modules.
# The script is designed to be run as a standalone program.
# The greet function can be used in other parts of the application
# or in different modules by importing it.
